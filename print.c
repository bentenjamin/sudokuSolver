#include "./sudoku.h"

void	putgrid(int grid[9][9])
{
	int	i;
	int	k;

	i = 0;
	while (i <= 8)
	{
		k = 0;
		while (k <= 8)
		{
			ft_putnbr(grid[i][k]);
			ft_putchar(' ');
			k++;
		}
		ft_putchar('\n');
		i++;
	}
}